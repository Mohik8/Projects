static const uint8_t RCON[11] = {
    0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36
};

/* ── GF(2^8) multiplication ──────────────────────────────────────────── */

static uint8_t gmul(uint8_t a, uint8_t b)
{
    uint8_t p = 0;
    for (int i = 0; i < 8; i++) {
        if (b & 1) p ^= a;
        uint8_t hi = a & 0x80;
        a <<= 1;
        if (hi) a ^= 0x1b;
        b >>= 1;
    }
    return p;
}

/* ── Key expansion ───────────────────────────────────────────────────── */

void aes128_init(AES128_CTX *ctx, const uint8_t key[AES128_KEY])
{
    uint8_t *rk = ctx->rk;
    memcpy(rk, key, 16);

    for (int i = 1; i <= 10; i++) {
        uint8_t *prev = rk + (i-1)*16;
        uint8_t *cur  = rk + i*16;

        /* RotWord + SubWord + Rcon on last word of previous round */
        uint8_t t[4] = {
            SBOX[prev[13]] ^ RCON[i],
            SBOX[prev[14]],
            SBOX[prev[15]],
            SBOX[prev[12]]
        };

        cur[ 0] = prev[ 0] ^ t[0];
        cur[ 1] = prev[ 1] ^ t[1];
        cur[ 2] = prev[ 2] ^ t[2];
        cur[ 3] = prev[ 3] ^ t[3];

        for (int j = 4; j < 16; j++)
            cur[j] = prev[j] ^ cur[j-4];
    }
}

/* ── AES state helpers (column-major 4×4 state) ──────────────────────── */

#define S(r,c) state[(c)*4+(r)]

static void add_round_key(uint8_t state[16], const uint8_t *rk)
{
    for (int i = 0; i < 16; i++) state[i] ^= rk[i];
}

static void sub_bytes(uint8_t state[16])
{
    for (int i = 0; i < 16; i++) state[i] = SBOX[state[i]];
}

static void inv_sub_bytes(uint8_t state[16])
{
    for (int i = 0; i < 16; i++) state[i] = INV_SBOX[state[i]];
}

static void shift_rows(uint8_t state[16])
{
    uint8_t t;
    /* row 1: rotate left 1 */
    t=S(1,0); S(1,0)=S(1,1); S(1,1)=S(1,2); S(1,2)=S(1,3); S(1,3)=t;
    /* row 2: rotate left 2 */
    t=S(2,0); S(2,0)=S(2,2); S(2,2)=t;
    t=S(2,1); S(2,1)=S(2,3); S(2,3)=t;
    /* row 3: rotate left 3 (= right 1) */
    t=S(3,3); S(3,3)=S(3,2); S(3,2)=S(3,1); S(3,1)=S(3,0); S(3,0)=t;
}

static void inv_shift_rows(uint8_t state[16])
{
    uint8_t t;
    t=S(1,3); S(1,3)=S(1,2); S(1,2)=S(1,1); S(1,1)=S(1,0); S(1,0)=t;
    t=S(2,0); S(2,0)=S(2,2); S(2,2)=t;
    t=S(2,1); S(2,1)=S(2,3); S(2,3)=t;
    t=S(3,0); S(3,0)=S(3,1); S(3,1)=S(3,2); S(3,2)=S(3,3); S(3,3)=t;
}

static void mix_columns(uint8_t state[16])
{
    for (int c = 0; c < 4; c++) {
        uint8_t s0=S(0,c), s1=S(1,c), s2=S(2,c), s3=S(3,c);
        S(0,c) = gmul(0x02,s0)^gmul(0x03,s1)^s2^s3;
        S(1,c) = s0^gmul(0x02,s1)^gmul(0x03,s2)^s3;
        S(2,c) = s0^s1^gmul(0x02,s2)^gmul(0x03,s3);
        S(3,c) = gmul(0x03,s0)^s1^s2^gmul(0x02,s3);
    }
}

static void inv_mix_columns(uint8_t state[16])
{
    for (int c = 0; c < 4; c++) {
        uint8_t s0=S(0,c), s1=S(1,c), s2=S(2,c), s3=S(3,c);
        S(0,c) = gmul(0x0e,s0)^gmul(0x0b,s1)^gmul(0x0d,s2)^gmul(0x09,s3);
        S(1,c) = gmul(0x09,s0)^gmul(0x0e,s1)^gmul(0x0b,s2)^gmul(0x0d,s3);
        S(2,c) = gmul(0x0d,s0)^gmul(0x09,s1)^gmul(0x0e,s2)^gmul(0x0b,s3);
        S(3,c) = gmul(0x0b,s0)^gmul(0x0d,s1)^gmul(0x09,s2)^gmul(0x0e,s3);
    }
}

/* ── Block encrypt / decrypt ─────────────────────────────────────────── */

void aes128_encrypt_block(const AES128_CTX *ctx,
                          const uint8_t in[AES_BLOCK], uint8_t out[AES_BLOCK])
{
    uint8_t state[16];
    memcpy(state, in, 16);

    add_round_key(state, ctx->rk);
    for (int r = 1; r < 10; r++) {
        sub_bytes(state);
        shift_rows(state);
        mix_columns(state);
        add_round_key(state, ctx->rk + r*16);
    }
    sub_bytes(state);
    shift_rows(state);
    add_round_key(state, ctx->rk + 160);

    memcpy(out, state, 16);
}

void aes128_decrypt_block(const AES128_CTX *ctx,
                          const uint8_t in[AES_BLOCK], uint8_t out[AES_BLOCK])
{
    uint8_t state[16];
    memcpy(state, in, 16);

    add_round_key(state, ctx->rk + 160);
    for (int r = 9; r >= 1; r--) {
        inv_shift_rows(state);
        inv_sub_bytes(state);
        add_round_key(state, ctx->rk + r*16);
        inv_mix_columns(state);
    }
    inv_shift_rows(state);
    inv_sub_bytes(state);
    add_round_key(state, ctx->rk);

    memcpy(out, state, 16);
}

/* ── ECB mode ────────────────────────────────────────────────────────── */

void aes128_ecb_encrypt(const AES128_CTX *ctx,
                        const uint8_t *in, uint8_t *out, size_t len)
{
    for (size_t i = 0; i < len; i += AES_BLOCK)
        aes128_encrypt_block(ctx, in + i, out + i);
}

void aes128_ecb_decrypt(const AES128_CTX *ctx,
                        const uint8_t *in, uint8_t *out, size_t len)
{
    for (size_t i = 0; i < len; i += AES_BLOCK)
        aes128_decrypt_block(ctx, in + i, out + i);
}

/* ── CBC mode ────────────────────────────────────────────────────────── */

void aes128_cbc_encrypt(const AES128_CTX *ctx, const uint8_t iv[AES_BLOCK],
                        const uint8_t *in, uint8_t *out, size_t len)
{
    uint8_t prev[AES_BLOCK];
    memcpy(prev, iv, AES_BLOCK);

    for (size_t i = 0; i < len; i += AES_BLOCK) {
        uint8_t xored[AES_BLOCK];
        for (int j = 0; j < AES_BLOCK; j++)
            xored[j] = in[i+j] ^ prev[j];
        aes128_encrypt_block(ctx, xored, out + i);
        memcpy(prev, out + i, AES_BLOCK);
    }
}

void aes128_cbc_decrypt(const AES128_CTX *ctx, const uint8_t iv[AES_BLOCK],
                        const uint8_t *in, uint8_t *out, size_t len)
{
    uint8_t prev[AES_BLOCK];
    memcpy(prev, iv, AES_BLOCK);

    for (size_t i = 0; i < len; i += AES_BLOCK) {
        /* Save ciphertext BEFORE potentially overwriting it (in-place safe) */
        uint8_t ct[AES_BLOCK], plain[AES_BLOCK];
        memcpy(ct, in + i, AES_BLOCK);
        aes128_decrypt_block(ctx, ct, plain);
        for (int j = 0; j < AES_BLOCK; j++)
            out[i+j] = plain[j] ^ prev[j];
        memcpy(prev, ct, AES_BLOCK);
    }
}
